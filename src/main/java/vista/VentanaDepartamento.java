    public void ActualizarTabla() {
        var header = new String[]{"Nombre", "Jefe departamental", "Sueldo", 
            "No Empleados", "AÃ±o de Inaguracion", "Empresa", "Codigo"};
        var data = new Object[this.departamentoControl.listar().size()][7];
        for (var i = 0; i < this.departamentoControl.listar().size(); i++) {
            data[i][0] = this.departamentoControl.listar().get(i).getNombre();
            data[i][2] = String.valueOf(this.departamentoControl.listar().get(i).getSueldo());
            data[i][1] = this.departamentoControl.listar().get(i).getJefeDepartamento();
            data[i][4] = Integer.toString(this.departamentoControl.listar().get(i).
                    getYearInaguracion().getYear()) + "-" + 
                    Integer.toString(this.departamentoControl.listar().get(i).
                            getYearInaguracion().getMonthValue()) + "-" + 
                    Integer.toString(this.departamentoControl.listar().get(i).
                            getYearInaguracion().getDayOfMonth());
            data[i][3] = Integer.toString(this.departamentoControl.listar().get(i).getNumeroEmpleados());
            data[i][5] = this.departamentoControl.listar().get(i).getEmpresa().getNombre();
            data[i][6] = Integer.toString(this.departamentoControl.listar().get(i).getCodigo());

        }

        this.tableModel = new DefaultTableModel(data, header);
        this.jTable1.setModel(this.tableModel);
    }

