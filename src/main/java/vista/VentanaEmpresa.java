    public void ActualizarTabla() {
        var header = new String[]{"Nombre", "Due침o", "A침o de creacion", "Empleados", "Gastos", "Codigo"};
        var data = new Object[this.empresaControl.listar().size()][6];
        for (var i = 0; i < this.empresaControl.listar().size(); i++) {
            data[i][0] = this.empresaControl.listar().get(i).getNombre();
            data[i][1] = this.empresaControl.listar().get(i).getDuenio();
            data[i][2] = Integer.toString(this.empresaControl.listar().
                    get(i).getAnioCreacion().getYear()) + "-" + Integer.
                            toString(this.empresaControl.listar().get(i).
                                    getAnioCreacion().getMonthValue()) + "-" 
                    + Integer.toString(this.empresaControl.listar().get(i).getAnioCreacion().getDayOfMonth());
            data[i][3] = Integer.toString(this.empresaControl.listar().get(i).getNumeroEmpleados());
            data[i][4] = String.valueOf(this.empresaControl.listar().get(i).getIngresosMensuales());
            data[i][5] = Integer.toString(this.empresaControl.listar().get(i).getCodigo());

        }

        this.tableModel = new DefaultTableModel(data, header);
        this.jTable2.setModel(this.tableModel);

    }

